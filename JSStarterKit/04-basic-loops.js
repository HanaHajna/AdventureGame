// Count from 1 to 5
for (let count = 1; count <= 5; count++) {
    console.log(count);
}
console.log("Counting complete!");


// Keep adding numbers until we reach 10
let sum = 0;
let target = 10;
while (sum < target) {
    sum = sum + 2;
    console.log("Sum is now: " + sum);
}
console.log("Reached the target!");